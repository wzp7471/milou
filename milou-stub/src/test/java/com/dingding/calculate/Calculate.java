package com.dingding.calculate;

public interface Calculate {
    /**
     * a+b
     * 
     * @param a
     * @param b
     * @return
     */
    int add(int a, int b);

    /**
     * a-b
     * 
     * @param a
     * @param b
     * @return
     */
    int substract(int a, int b);

    /**
     * a*b
     * 
     * @param a
     * @param b
     * @return
     */
    int multiply(int a, int b);
}
